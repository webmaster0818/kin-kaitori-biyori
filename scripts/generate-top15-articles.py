def faq_schema(faqs):
    """Convert list of (q,a) tuples to FAQPage schema."""
    data = {
        "@context": "https://schema.org", "@type": "FAQPage",
        "mainEntity": [{"@type": "Question", "name": q, "acceptedAnswer": {"@type": "Answer", "text": a}} for q, a in faqs],
    }
    return json.dumps(data, ensure_ascii=False)


def faq_html(faqs):
    """FAQ accordion HTML."""
    items = []
    for q, a in faqs:
        items.append(
            f'<details className="border-b border-warm-border py-4 group not-prose">\n'
            f'  <summary className="font-bold cursor-pointer flex justify-between items-center"><span>{q}</span><span className="text-2xl ml-4 group-open:rotate-45 transition-transform">+</span></summary>\n'
            f'  <p className="mt-3 text-sm leading-relaxed">{a}</p>\n'
            f'</details>'
        )
    return "\n".join(items)


